function replace_blanks($str) {
    $result = $str;
    for ($i = 0; $i < strlen($result); $i++) {
        if ($result[$i] == ' ') {
            $result[$i] = '_';
        }
    }
    return $result;
}

function replace_blanks2($str) {
    return str_replace(' ', '_', $str);
}

