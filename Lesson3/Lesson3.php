function transliterate($str) {
    $result = [];
    for ($i = 0; $i < mb_strlen($str); $i++) {
        $ch = mb_substr($str, $i, 1);
        $result[$i] = array_key_exists($ch, TRANSLIT) ? TRANSLIT[$ch] : $ch;
    }
    return implode($result);
}

